"""
+, -, *, /, //, **, %, ()
"""

print('Multiplicação', 10*10)
print('Adição', 10+10)
print('Subtração', 10-10)
print('Divisão', 10 / 2)
print('Divisao inteira', 10 // 3)
print('Exponenciação', 10 ** 5)
print('Módulo ou resto da divisão!  ', 10 % 3)

print('Com precedencia', (10 + 5) * 10)
print('Sem precedencia', 10 + 5 * 10)

